/* This file illustrates the ISO loop Application example using the DMA AUTO mode */

/*
   This example illustrates a loopback mechanism between two USB isochronous endpoints.
   The example comprises of vendor class USB enumeration descriptors with two isochronous
   endpoints.

   A ISO OUT endpoint acts as the producer of data from the host. A ISO IN endpint acts as
   the consumer of data to the host. The loopback is achieved with the help of a DMA AUTO
   channel. DMA Auto channel is created between the producer USB ISO endpoint and the consumer
   USB ISO endpoint. Data is transferred from the host into the producer endpoint which is 
   then directly transferred to the consumer endpoint by the DMA engine. CPU is not involved
   in the data transfer.

   The DMA buffer size is defined based on the USB speed. 64 for full speed, 1024 for high
   speed and 1024 for super speed. CY_FX_ISOLP_DMA_BUF_COUNT in the header file defines the
   number of DMA buffers.
 */

